nome = "Paulo Henrique Alves Santana"

print(nome[0]) # primeira letra da String

print(nome[:5]) # do inicio até a quinta casa

print(nome[6:]) # da sexta casa até o final da String

print(nome[6:20]) # da sexta casa até a vigésima casa da String

print(nome[6:20:2]) # da sexta casa até a vigésima casa da String, pulando duas casas

print(nome[:]) # apresenta todo o valor da String

print(nome[::-1]) # apresenta todo o valor da String, realizando o espelhamento da mesma